  // Helper functions for language operations
  export const getLanguageByCode = (code) => {
    return LANGUAGES.find(lang => lang.code === code);
  };
  
  export const searchLanguages = (searchTerm) => {
    if (!searchTerm) return LANGUAGES;
    
    const term = searchTerm.toLowerCase();
    return LANGUAGES.filter(lang => 
      lang.name.toLowerCase().includes(term) ||
      lang.continent.toLowerCase().includes(term)
    );
  };
  
  export const getLanguagesByContinent = (continent) => {
    return LANGUAGES.filter(lang => lang.continent === continent);
  };
  
  export const getMostSpokenLanguages = (limit = 20) => {
    return [...LANGUAGES]
      .sort((a, b) => b.speakers - a.speakers)
      .slice(0, limit);
  };